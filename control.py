# If Statement
number = 10
if number > 0:
    print("The number is positive.")
elif number == 0:
    print("The number is zero.")
else:
    print("The number is negative.")
# For Loop
for i in range(5):
    print("Iteration:", i)
# While Loop
count = 0
while count < 5:
    print("Count:", count)
    count += 1
